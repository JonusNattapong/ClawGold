def get_db() -> SqliteDatabase:
    """Get database instance."""
    return db


def create_tables():
    """Create all tables."""
    db.create_tables([
        Trade, Signal, AgentExecution,
        NewsItem, SentimentRecord, DailyMetrics
    ], safe=True)
    logger.info(f"[Peewee] Tables created/verified at {DB_PATH}")


def drop_tables():
    """Drop all tables (destructive)."""
    db.drop_tables([
        Trade, Signal, AgentExecution,
        NewsItem, SentimentRecord, DailyMetrics
    ], safe=True)
    logger.warning("[Peewee] All tables dropped")


# ─────────────────────────────────────────────────────
# Query Helpers
# ─────────────────────────────────────────────────────

class QueryHelpers:
    """Common query patterns."""
    
    @staticmethod
    def get_trades_today() -> List[Trade]:
        """Get all trades from today."""
        today = datetime.now().date()
        return Trade.select().where(
            fn.DATE(Trade.created_at) == today
        )
    
    @staticmethod
    def get_total_pnl(days: int = 7) -> float:
        """Get total P&L for last N days."""
        cutoff = datetime.now() - timedelta(days=days)
        trades = Trade.select().where(
            (Trade.status == TradeStatus.CLOSED) &
            (Trade.created_at >= cutoff)
        )
        return sum(t.pnl for t in trades)
    
    @staticmethod
    def get_win_rate(days: int = 7) -> float:
        """Calculate win rate for last N days."""
        cutoff = datetime.now() - timedelta(days=days)
        trades = Trade.select().where(
            (Trade.status == TradeStatus.CLOSED) &
            (Trade.created_at >= cutoff)
        )
        trades = list(trades)
        if not trades:
            return 0.0
        winners = sum(1 for t in trades if t.pnl > 0)
        return winners / len(trades)
    
    @staticmethod
    def get_recent_signals(limit: int = 10) -> List[Signal]:
        """Get recent trading signals."""
        return Signal.select().order_by(Signal.created_at.desc()).limit(limit)
    
    @staticmethod
    def get_agent_stats(tool: str, days: int = 7) -> Dict[str, Any]:
        """Get stats for an AI tool."""
        cutoff = datetime.now() - timedelta(days=days)
        executions = AgentExecution.select().where(
            (AgentExecution.tool == tool) &
            (AgentExecution.created_at >= cutoff)
        )
        executions = list(executions)
        
        if not executions:
            return {}
        
        total_cost = sum(e.cost_usd for e in executions)
        avg_cost = total_cost / len(executions)
        success_rate = sum(1 for e in executions if e.success) / len(executions)
        avg_duration = sum(e.duration_ms for e in executions) / len(executions)
        
        return {
            'tool': tool,
            'calls': len(executions),
            'total_cost': total_cost,
            'avg_cost': avg_cost,
            'success_rate': success_rate,
            'avg_duration_ms': avg_duration,
        }
