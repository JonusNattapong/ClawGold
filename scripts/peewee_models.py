import os
from datetime import datetime, timedelta
from typing import Optional, List, Dict, Any
from enum import Enum

from peewee import (
    SqliteDatabase, Model, CharField, FloatField, IntegerField,
    DateTimeField, BooleanField, TextField, ForeignKeyField, fn
)
from playhouse.sqlite_ext import JSONField

from logger import get_logger

logger = get_logger(__name__)


# ─────────────────────────────────────────────────────
# Database Configuration
# ─────────────────────────────────────────────────────

DB_PATH = os.path.join("data", "clawgold.db")
os.makedirs("data", exist_ok=True)

db = SqliteDatabase(DB_PATH)


# ─────────────────────────────────────────────────────
# Enums
# ─────────────────────────────────────────────────────

class TradeStatus(str, Enum):
    """Trade status states."""
    PENDING = "pending"
    OPEN = "open"
    CLOSED = "closed"
    CANCELLED = "cancelled"


class SignalStatus(str, Enum):
    """Signal status states."""
    ACTIVE = "active"
    EXECUTED = "executed"
    EXPIRED = "expired"
    CANCELLED = "cancelled"


# ─────────────────────────────────────────────────────
# Base Model
# ─────────────────────────────────────────────────────

class BaseModel(Model):
    """Base model with common timestamp fields."""
    created_at = DateTimeField(default=datetime.now)
    updated_at = DateTimeField(default=datetime.now)
    
    class Meta:
        database = db
    
    def save(self, *args, **kwargs):
        """Update timestamp on save."""
        self.updated_at = datetime.now()
        return super().save(*args, **kwargs)


# ─────────────────────────────────────────────────────
# Trade Model
# ─────────────────────────────────────────────────────

class Trade(BaseModel):
    """Completed or in-progress trade record."""
    
    symbol = CharField(default="XAUUSD", help_text="Trading symbol (XAUUSD)")
    status = CharField(default=TradeStatus.OPEN, help_text="Trade status")
    
    # Entry details
    entry_price = FloatField(help_text="Entry price (USD)")
    entry_time = DateTimeField(default=datetime.now, help_text="Entry timestamp")
    entry_reason = TextField(default="", help_text="Why entered (signal description)")
    
    # Exit details
    exit_price = FloatField(null=True, help_text="Exit price (USD)")
    exit_time = DateTimeField(null=True, help_text="Exit timestamp")
    exit_reason = TextField(default="", help_text="Why exited (TP/SL/manual)")
    
    # Position details
    quantity = FloatField(help_text="Position size (lots)")
    leverage = IntegerField(default=1, help_text="Leverage used")
    
    # Risk management
    stop_loss = FloatField(null=True, help_text="Stop loss level")
    take_profit = FloatField(null=True, help_text="Take profit level")
    
    # Performance
    pnl = FloatField(default=0.0, help_text="Profit/Loss (USD)")
    pnl_percent = FloatField(default=0.0, help_text="Profit/Loss %")
    
    # Metadata
    metadata = JSONField(default={}, help_text="Custom metadata (strategy, etc.)")
    
    class Meta:
        table_name = "trades"
    
    def calculate_pnl(self):
        """Calculate P&L if exit price is set."""
        if self.exit_price:
            pnl = (self.exit_price - self.entry_price) * self.quantity * 100
            pnl_pct = ((self.exit_price - self.entry_price) / self.entry_price) * 100
            self.pnl = pnl
            self.pnl_percent = pnl_pct
            return pnl
        return 0.0


