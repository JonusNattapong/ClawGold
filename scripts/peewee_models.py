# Signal Model
# ─────────────────────────────────────────────────────

class Signal(BaseModel):
    """Trading signal generated by AI agents."""
    
    symbol = CharField(default="XAUUSD")
    signal_type = CharField(help_text="BUY, SELL, HOLD, NEUTRAL")
    confidence = FloatField(help_text="Confidence 0-1")
    status = CharField(default=SignalStatus.ACTIVE)
    
    # Signal details
    reason = TextField(help_text="Why this signal")
    target_price = FloatField(null=True, help_text="Target price")
    stop_loss = FloatField(null=True, help_text="Recommended SL")
    
    # Execution
    executed = BooleanField(default=False, help_text="Was this signal traded?")
    executed_time = DateTimeField(null=True, help_text="When trade was executed")
    trade = ForeignKeyField(Trade, null=True, backref="signals", help_text="Associated trade")
    
    # Quality tracking
    quality_score = FloatField(null=True, help_text="Post-mortem quality score 0-1")
    actual_outcome = CharField(null=True, help_text="Actual result (profitable/loss/neutral)")
    
    # Metadata
    agent = CharField(default="", help_text="Agent that generated signal")
    metadata = JSONField(default={})
    
    class Meta:
        table_name = "signals"


# ─────────────────────────────────────────────────────
# Agent Execution Model
# ─────────────────────────────────────────────────────

class AgentExecution(BaseModel):
    """Record of AI agent task execution."""
    
    task_name = CharField(help_text="Task executed (daily_report, etc.)")
    agent = CharField(help_text="Agent name (researcher, analyst, etc.)")
    tool = CharField(help_text="Tool used (opencode, gemini, etc.)")
    
    # Execution details
    prompt = TextField(help_text="Input prompt")
    response = TextField(help_text="Generated response")
    
    # Performance
    duration_ms = IntegerField(help_text="Execution time in milliseconds")
    tokens_in = IntegerField(default=0, help_text="Input tokens")
    tokens_out = IntegerField(default=0, help_text="Output tokens")
    cost_usd = FloatField(default=0.0, help_text="API cost in USD")
    
    # Status
    success = BooleanField(default=True, help_text="Did execution succeed?")
    error_message = TextField(default="", help_text="Error if failed")
    
    # Evaluation
    quality_score = FloatField(null=True, help_text="Quality evaluation 0-1")
    
    # Metadata
    metadata = JSONField(default={})
    
    class Meta:
        table_name = "agent_executions"
    
    @classmethod
    def log_execution(cls, task_name: str, agent: str, tool: str, 
                      prompt: str, response: str, duration_ms: int,
                      tokens_in: int = 0, tokens_out: int = 0, cost: float = 0.0,
                      success: bool = True, error: str = ""):
        """Convenience method to log an execution."""
        return cls.create(
            task_name=task_name,
            agent=agent,
            tool=tool,
            prompt=prompt,
            response=response,
            duration_ms=duration_ms,
            tokens_in=tokens_in,
            tokens_out=tokens_out,
            cost_usd=cost,
            success=success,
            error_message=error,
        )


